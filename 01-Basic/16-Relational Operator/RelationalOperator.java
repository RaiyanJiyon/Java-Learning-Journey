public class RelationalOperator {
    public static void main(String[] args) {
        // Declare and initialize two integer variables
        int a = 20;
        int b = 10;

        // Compare a and b using relational operators and print the results
        System.err.println(a > b);  // Greater than
        System.err.println(a >= b); // Greater than or equal to
        System.err.println(a < b);  // Less than
        System.err.println(a <= b); // Less than or equal to
        System.err.println(a == b); // Equal to
        System.err.println(a != b); // Not equal to
    }
}
