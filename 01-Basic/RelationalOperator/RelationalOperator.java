package RelationalOperator;

public class RelationalOperator {
    public static void main(String[] args) {
        int a = 20;
        int b = 10;

        System.err.println(a>b);
        System.err.println(a>=b);
        System.err.println(a<b);
        System.err.println(a<=b);
        System.err.println(a==b);
        System.err.println(a!=b);
    }
}
